'''
프레임워크: 프로그래밍의 뼈대가 이미 준비되어 있어 빈 곳에 내가 작성할 코드만 작성하면 됨.
-> 이미 반은 완성된 프로그램이다!
-> 큰 틀이 정해져 있고 세부 내용이 바뀌는 프로그램에 프레임워크가 효과적임.
    ex) 비밀번호 암호화...등 보안/암호화같이 까다롭고 어려운 부분은 프레임워크로 하는 게 좋당.
-> 프레임워크의 구조를 바꾸는 건 웬만하면 어렵다는 단점이 존재

ex) Ruby 언어의 Rails 프레임워크.
'''

'''
라이브러리 vs. 프레임워크 -----------------------------------------------------------------------------
- 라이브러리: 이미 누가 만들어 둔 도구를 가져와서 사용. ex) 카페로 따지면 커피머신을 사용하는 것.
- 프레임워크: 최소한의 노력으로 프로그램 완성 가능.
        ex) 카페로 따지면 이미 매뉴얼이 다 정해져 있는 프차카페를 오픈하는 것.
'''